// Fallback image for any missing articles
export const defaultStockImage = 'https://images.unsplash.com/photo-1486312338219-ce68d2c6f44d?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&q=80';

/**
 * Get the appropriate stock image for an article
 * @param {string} categoryKey - The category key
 * @param {string} articleId - The article ID
 * @returns {string} The image URL
 */
export const getArticleImage = (categoryKey, articleId) => {
  const categoryImages = stockImages[categoryKey];
  if (!categoryImages) return defaultStockImage;
  
  return categoryImages[articleId] || categoryImages.default || defaultStockImage;
};

/**
 * Get the default image for a category
 * @param {string} categoryKey - The category key
 * @returns {string} The image URL
 */
export const getCategoryImage = (categoryKey) => {
  const categoryImages = stockImages[categoryKey];
  return categoryImages?.default || defaultStockImage;
}; 